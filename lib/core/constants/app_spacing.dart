/// Femman spacing system based on 8px grid.
/// All spacing values follow multiples of 8 for consistent rhythm.
class AppSpacing {
  AppSpacing._();

  static const double xs = 4.0; // 0.5 × base unit
  static const double sm = 8.0; // 1 × base unit
  static const double md = 16.0; // 2 × base unit
  static const double lg = 24.0; // 3 × base unit
  static const double xl = 32.0; // 4 × base unit
  static const double xxl = 48.0; // 6 × base unit
}
